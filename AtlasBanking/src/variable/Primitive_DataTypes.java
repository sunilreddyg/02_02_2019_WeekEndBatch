package variable;

public class Primitive_DataTypes {

	public static void main(String[] args)
	{
		
		/*
		 * int:==> only store numbers
		 * 			default value is zero.
		 */
		
		 int a=100;
		 int b=200;
		 
		 int c=a+b;
		 System.out.println("total  is => "+c);
		 
		 
		 /*
		  * boolean:-->
		  * 		Store only true/false
		  * 		Default value is false.
		  */
		 boolean flag=true;
		 System.out.println("boolean status is => "+flag);
		 
		 
	}

}
